a = "anant is genius.\nbut he is lazy." #\n this is a newline character
print(a)  # print the string

b = "anant\tkumar"  # \t this is a tab character
print(b)  # print the string with tab

c = "\"honesty is the best policy\""  # \" this is an escape character for double quotes
print(c)  # print the string with double quote
