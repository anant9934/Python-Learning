#Arithmetic Operators
a = 10
b = 20
c = (a + b)  # Addition
print(c)

#assignment Operators
a = 10
b = 20
a += 10 # a = a + 10
b -= 5  # b = b - 5
print(a, b)

# Comparison Operators
a = 10
b = 20
print(a == b)  # Equal to
print(a != b)  # Not equal to
print(a > b)   # Greater than
print(a < b)   # Less than
print(a >= b)  # Greater than or equal to
print(a <= b)  # Less than or equal to

# Logical Operators
a = True
b = False
print(a and b)  # Logical AND which returns True if both are True
print(a or b)   # Logical OR which returns True if at least one is True
print(not a)    # Logical NOT which returns the opposite boolean value     