# for loops with lists
a = [55, 87, 00, 99, 100, 101, 102, 103, 104, 105]
for i in a:  # Iterate through each element in the list
    print(i)  # Print each element

# for loops with strings
s = "Hello World"
for i in s:  # Iterate through each character in the string
    print(i)  # Print each character

# for loops with tuples
t = (1, 2, 3, 4, 5)
for i in t:  # Iterate through each element in the tuple
    print(i)  # Print each element